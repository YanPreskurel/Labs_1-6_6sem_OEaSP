#!/bin/bash

# Функция для генерации случайных чисел на основе мультипликативного алгоритма
random() {
    seed=$(($1 * 1103515245 + 12345))
    echo $(($seed % $2))
}

# Инициализация генератора случайных чисел от текущего времени
seed=$(date +%s)

# Главный цикл программы
while true; do
    clear  # Очистить экран консоли

    # Получить текущее время
    current_time=$(date +"%T")

    # Генерировать случайные координаты для отображения часов
    x=$(random $seed $(tput cols))
    y=$(random $seed $(tput lines))

    # Вывести текстовые часы в случайных координатах
    tput cup $y $x
    echo $current_time

    # Обновить seed для генератора случайных чисел
    seed=$(($seed + 1))

    # Подождать 10 секунд перед следующим обновлением
    sleep 10
done
